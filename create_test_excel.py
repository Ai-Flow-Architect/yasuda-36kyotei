def create_test_excel(output_path: str = "demo_data/test_all7forms.xlsx"):
    wb = openpyxl.Workbook()
    ws = wb.active
    ws.title = "36協定テストデータ（全7様式）"

    # ─── ヘッダー行（1行目）
    header_fill = PatternFill(start_color="1F4E79", end_color="1F4E79", fill_type="solid")
    header_font = Font(color="FFFFFF", bold=True, size=9)
    header_align = Alignment(horizontal="center", vertical="center", wrap_text=True)

    for col_idx, header in enumerate(HEADERS, start=1):
        cell = ws.cell(row=1, column=col_idx, value=header)
        cell.fill = header_fill
        cell.font = header_font
        cell.alignment = header_align

    # 様式パターン上書き列（AS=45）を目立つ色にする
    ws.cell(row=1, column=45).fill = PatternFill(start_color="C00000", end_color="C00000", fill_type="solid")

    # ─── データ行（2〜8行目）
    row_colors = [
        "E2EFDA",  # 様式9   : 薄緑
        "DDEBF7",  # 様式9_2 : 薄青
        "FFF2CC",  # 様式9_3 : 薄黄
        "FCE4D6",  # 様式9_4 : 薄オレンジ
        "E8D5F5",  # 様式9_5 : 薄紫
        "D9E1F2",  # 様式10  : 薄藍
        "F4CCCC",  # 様式10_2: 薄ピンク
    ]

    for row_idx, record in enumerate(TEST_RECORDS, start=2):
        fill_color = row_colors[row_idx - 2]
        row_fill = PatternFill(start_color=fill_color, end_color=fill_color, fill_type="solid")

        for col_idx, header in enumerate(HEADERS, start=1):
            value = record.get(header, "")
            cell = ws.cell(row=row_idx, column=col_idx, value=value)
            cell.fill = row_fill
            cell.font = Font(size=9)
            cell.alignment = Alignment(vertical="center", wrap_text=False)

    # ─── 列幅の調整
    ws.column_dimensions["A"].width = 8   # 更新月
    ws.column_dimensions["D"].width = 28  # 事業所名
    ws.column_dimensions["E"].width = 14  # 事業主名
    ws.column_dimensions["G"].width = 16  # 事業の種類
    ws.column_dimensions["I"].width = 20  # 業務の種類
    ws.column_dimensions["T"].width = 20  # 始業終業時刻
    ws.column_dimensions["AS"].width = 16 # 様式上書き

    # ─── 行の高さ
    ws.row_dimensions[1].height = 30
    for r in range(2, 9):
        ws.row_dimensions[r].height = 20

    # ─── 列ヘッダーを凍結（A1を固定）
    ws.freeze_panes = "A2"

    # ─── 保存
    Path(output_path).parent.mkdir(parents=True, exist_ok=True)
    wb.save(output_path)
    print(f"✅ テストExcel作成完了: {output_path}")
    print(f"   行数: {len(TEST_RECORDS)} 件（様式9/9_2/9_3/9_4/9_5/10/10_2）")
    return output_path


if __name__ == "__main__":
    output = sys.argv[1] if len(sys.argv) > 1 else "demo_data/test_all7forms.xlsx"
    create_test_excel(output)
