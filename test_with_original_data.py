# ── 類似度計算関数群 ─────────────────────────────────────────
KANJI_NUM = {"一":"1","二":"2","三":"3","四":"4","五":"5","六":"6","七":"7","八":"8","九":"9","〇":"0","十":"10"}

def kanji_to_arabic(s):
    s = re.sub(r'十([一二三四五六七八九])', lambda m: str(10 + int(KANJI_NUM[m.group(1)])), s)
    s = re.sub(r'([二三四五六七八九])十', lambda m: str(int(KANJI_NUM[m.group(1)]) * 10), s)
    s = re.sub(r'十', '10', s)
    for k, v in KANJI_NUM.items():
        s = s.replace(k, v)
    return s

def normalize_art_num(text):
    # S1: 「第8 条」のようにスペースが挟まるPDF抽出ノイズを除去
    text = re.sub(r'第(\d+)\s+条', r'第\1条', text)
    def replace_art(m):
        inner = m.group(1).translate(str.maketrans('０１２３４５６７８９','0123456789'))
        return f'第{kanji_to_arabic(inner)}条'
    return re.sub(r'第([一二三四五六七八九〇十０-９0-9]+)条', replace_art, text)

def mask_variables(text):
    # S2: PDFテキスト抽出の改行・空白ノイズを正規化
    # テーブル内のセル折り返し行差異を吸収するため、全改行をスペースに統合
    text = re.sub(r'[ \t　\n\r]+', ' ', text)  # 全ホワイトスペース → 半角1つ
    # S18: 全角コロン（：）→ スペース（手続：〇〇 vs 手続 〇〇 の差異を吸収）
    text = text.replace('：', ' ')
    # S19: 署名欄の構造ラベル（甲）（乙）を除去（原本と生成で位置が異なる場合を吸収）
    text = re.sub(r'（[甲乙]）\s*', '', text)
    # S20: 全角ASCIIアルファベット→半角（PDF抽出ノイズ: "ＷＥＢ" → "WEB", "Ｆ社" → "F社"）
    # ※ FF21-FF3A: 大文字A-Z、FF41-FF5A: 小文字a-z のみ変換。（）等の記号は変換しない
    text = ''.join(
        chr(ord(c) - 0xFEE0) if ('\uFF21' <= c <= '\uFF3A' or '\uFF41' <= c <= '\uFF5A') else c
        for c in text
    )
    # S18b: 半角コロン時刻区切り（8:00 → "8 00"）原本はスペース区切り、生成はコロン区切り
    text = re.sub(r'(\d{1,2}):(\d{2})(?!\d)', r'\1 \2', text)
    # S21: CJK文字間の空白除去（PDF抽出ノイズ: "させ る" → "させる", "具体 的事由" → "具体的事由"）
    # ※ 日本語は単語間スペース不要のため、CJK文字間のスペースはすべて除去して比較
    text = re.sub(r'(?<=[\u3040-\u9FFF\uFF00-\uFFEF])\s+(?=[\u3040-\u9FFF\uFF00-\uFFEF])', '', text)
    # 数字とCJKの間のスペースも除去（"NUM 歳以上" → "NUM歳以上" 等）
    text = re.sub(r'(?<=\d)\s+(?=[\u3040-\u9FFF\uFF00-\uFFEF])', '', text)
    text = re.sub(r'(?<=[\u3040-\u9FFF\uFF00-\uFFEF])\s+(?=\d)', '', text)
    # S21b: ASCII大文字とCJKの間のスペース除去（"WEB デザイン" → "WEBデザイン"）
    text = re.sub(r'(?<=[A-Z])\s+(?=[\u3040-\u9FFF\uFF00-\uFFEF])', '', text)
    # S21c: 丸付き数字とCJKの間のスペース除去（"① 下記" → "①下記"）
    text = re.sub(r'(?<=[\u2460-\u24FF])\s+(?=[\u3040-\u9FFF\uFF00-\uFFEF])', '', text)
    text = re.sub(r'(?<=[\u3040-\u9FFF\uFF00-\uFFEF])\s+(?=[\u2460-\u24FF])', '', text)
    # S21d: 句点・読点後のスペース除去（"とする。 尚、" → "とする。尚、"）
    text = re.sub(r'([。、])\s+(?=[\u3040-\u9FFF\uFF00-\uFFEF])', r'\1', text)
    # S34: 「就業規則第の規定」→「就業規則の規定」（原本PDFで条番号なしの「第」が残留するノイズ）
    text = text.replace('就業規則第の規定', '就業規則の規定')
    # S31: ページ番号が「時間外」に直結したOCRノイズ除去
    # 原本PDF先頭の孤立したページ番号(例: "1\n")がS21後に"1時間外労働"になり
    # 数値マスクで"NUM外労働"になってしまう（生成側は"時間外労働"のまま）
    text = re.sub(r'\d+(?=時間外)', '', text)
    # S13: 単位名内の空白除去（PDF抽出ノイズ: "時 間" → "時間", "か 月" → "か月"）
    text = re.sub(r'時\s+間', '時間', text)
    text = re.sub(r'か\s+月', 'か月', text)
    text = re.sub(r'ヶ\s+月', 'ヶ月', text)
    # S14: 数字とCJK単位の間のスペース除去（PDF抽出ノイズ: "6 時" → "6時", "8 月" → "8月"）
    text = re.sub(r'(\d)\s+(時|分|秒|日|月|年|週|回|円|人|条)', r'\1\2', text)
    # S15: テーブルヘッダーのCJK文字間スペース（PDF抽出ノイズ: "始 業" → "始業"）
    text = re.sub(r'始\s+業', '始業', text)
    text = re.sub(r'終\s+業', '終業', text)
    text = re.sub(r'休\s+憩', '休憩', text)
    text = re.sub(r'業\s+務', '業務', text)
    text = re.sub(r'種\s+類', '種類', text)
    # S16: 波ダッシュ・チルダ統一（原本PDF: ～ U+FF5E、生成PDF: 〜 U+3030）+ 前後空白除去
    text = re.sub(r'\s*[～〜~]\s*', '〜', text)
    # S17: 中黒（・）正規化 — 原本PDFが箇条書きに使う U+30FB を除去（生成PDFにはない）
    text = text.replace('・', '')
    # 日付マスク（スペース混入対応）
    text = re.sub(r'令和\s*\d+\s*年\s*\d+\s*月\s*\d+\s*日', 'RDATE', text)
    text = re.sub(r'令和\s*\d+\s*年\s*\d+\s*月', 'RDATE', text)
    text = re.sub(r'令和[〇一二三四五六七八九十]+年[〇一二三四五六七八九十]+月[〇一二三四五六七八九十]+日', 'RDATE', text)
    text = re.sub(r'\d+[\.,]\d+', 'NUM', text)
    # 数値+単位（スペース混入対応: 「1 週」「80 時間」等）
    text = re.sub(r'\d+\s*(?:時間|分|回|%|％|円|人|日|週|ヶ月|か月|箇月)', 'NUM', text)
    text = re.sub(r'(?<![第条])\d+', 'NUM', text)
    text = re.sub(r'[０-９]+\s*(?:時間|分|回|％|円|人|日)', 'NUM', text)
    # S22: OCRアーティファクト「位置年」→「NUM年」（原本PDFで1年が位置年と誤認識される）
    text = text.replace('位置年', 'NUM年')
    # S23: 「前日」→「NUM前」（前日 = 1日前 の表現統一）
    text = text.replace('前日', 'NUM前')
    # S32: 署名欄日付の正規化（生成側は年号年が入るが原本は空欄の場合がある）
    # 「令和NUM年月日」（生成）← → 「令和年月日」（原本未記入）を統一
    text = re.sub(r'令和NUM年月日', '令和年月日', text)
    text = re.sub(r'令和年月日', 'RDATE', text)
    # S35: 条文本文中への有効期間ノイズ挿入を除去（原本PDFテキスト抽出の誤挿入）
    # D社第10条で「配転者RDATEからNUM年間NUM．パートタイマー」のように
    # 前後の文脈から切り離された「RDATEからNUM年間」が挿入されるパターンを除去
    text = re.sub(r'([^\s。、])RDATEからNUM年間(?=[^がとはにをも])', r'\1', text)
    # S36: 句点・読点の後のマスクトークン（NUM/RDATE等）前スペースを除去
    # S21dはCJK文字前のスペースのみ除去するが、マスクトークン（英字）前は残留する
    text = re.sub(r'([。、])\s+(NUM|RDATE|COMPANY|SIGN)(?=[^\d])', r'\1\2', text)
    # S37: 「NUM第N章」→「第N章」（原本PDFで章番号の前に列挙数字が付く場合の正規化）
    text = re.sub(r'NUM(?=第\d+章)', '', text)
    # S24: 時刻範囲「NUM時からNUM時」→「NUM時〜NUM時」（時間帯表記統一）
    text = re.sub(r'(NUM時(?:NUM分)?)から(NUM時)', r'\1〜\2', text)
    # S26: 列ヘッダーの「（満NUM歳以上の者）」括弧を除去（原本PDFは括弧なしの場合がある）
    text = re.sub(r'（満(NUM|\d+)歳以上の者）', r'満\1歳以上の者', text)
    # S27: 列ヘッダー「従事する労働者数」→「労働者数」（原本PDFは前置語なしの場合がある）
    text = text.replace('従事する労働者数', '労働者数')
    # S28: 署名欄ラベル統一（「氏名」「署名」→ SIGN）
    text = re.sub(r'(?:氏名|署名)', 'SIGN', text)
    # S29: 休日テーブル列ヘッダー「時間外/休日」労働をさせる必要 → 統一
    text = re.sub(r'(?:時間外|休日)労働をさせる必要のある', '労働をさせる必要のある', text)
    text = re.sub(r'(?:株式会社|有限会社|合同会社|一般社団法人|公益財団法人)\S{0,25}', 'COMPANY', text)
    # 逆順（〇〇株式会社形式）は上限20文字で過剰マッチを防止
    # S21でCJK間スペース削除後は\S+が文全体にマッチする誤爆が起きる（BUG-013）
    text = re.sub(r'\S{0,20}(?:株式会社|有限会社|合同会社)', 'COMPANY', text)
    # S25: 会社名残滓スペース除去（COMPANY後の "E 社" → "E社"）
    # ※ テストデータの"株式会社 E 社"と原本PDFの"株式会社Ｅ社"の差異を吸収
    text = re.sub(r'([A-Za-z])\s+社', r'\1社', text)
    # S30: ドライバーテーブルの乗務区分ラベル除去
    # 原本PDFには「1人乗務」「2人乗務」サブ行があり、数値マスク後「NUM乗務」が残留する
    # 生成PDFにはこのサブ行構造がないため比較時に差異が生じる → 双方から除去して揃える
    text = re.sub(r'NUM乗務', '', text)
    # S33: 特別条項テーブルのヘッダー末尾〜①の間の余分コンテンツを除去（BUG-014対策）
    # 生成PDF: "延長...時間数NUMNUMNUM年NUMNUM年①" (データ値がヘッダー後に来る)
    # 原本PDF: "延長...時間数延長...時間数①" (重複ヘッダーが来る)
    # 双方から「延長...時間数及び休日労働の時間数」の直後、「①」の直前のコンテンツを除去
    text = re.sub(
        r'(延長することができる時間数及び休日労働の時間数)'
        r'(?:(?:NUM+(?:年)?)+|延長することができる時間数)*'
        r'(?=①)',
        r'\1',
        text
    )
    # また「満NUM歳以上の者」直後の孤立NUM値群も除去（原本側のみに出るパターン）
    text = re.sub(
        r'(満NUM歳以上の者)'
        r'(?:NUM+(?:年)?)+(?=延長することができる時間数)',
        r'\1',
        text
    )
    return text

def split_arts_raw(text):
    headers = []
    for m in re.finditer(r'(?:^|\n)(第(\d+)条)(?![のにをはがでもへとからまでよりについて])', text):
        art_num = int(m.group(2))
        if art_num <= 25:
            headers.append((m.start(1), m.end(1), art_num))
    result = {}
    prev_end = 0
    prev_key = '前文'
    for start, end, art_num in headers:
        result[prev_key] = text[prev_end:start].strip()
        prev_key = f'第{art_num}条'
        prev_end = start
    result[prev_key] = text[prev_end:].strip()
    return result

def similarity(a, b):
    return difflib.SequenceMatcher(None, a, b).ratio()

def compare(orig_text, gen_text):
    orig_norm = normalize_art_num(orig_text)
    gen_norm  = normalize_art_num(gen_text)
    overall = similarity(mask_variables(orig_norm), mask_variables(gen_norm))
    orig_arts_raw = split_arts_raw(orig_norm)
    gen_arts_raw  = split_arts_raw(gen_norm)
    orig_arts = {k: mask_variables(v) for k, v in orig_arts_raw.items()}
    gen_arts  = {k: mask_variables(v) for k, v in gen_arts_raw.items()}
    all_keys = set(orig_arts) | set(gen_arts)
    art_sims = {}
    for key in all_keys:
        if key in orig_arts and key in gen_arts:
            art_sims[key] = similarity(orig_arts[key], gen_arts[key])
        elif key in orig_arts:
            art_sims[f"{key}(原本のみ)"] = 0.0
        else:
            art_sims[f"{key}(生成のみ)"] = 0.0
    art_avg = sum(art_sims.values()) / len(art_sims) if art_sims else 0.0
    return overall, art_avg, art_sims, len(orig_arts), len(gen_arts)

def extract_text(path):
    doc = fitz.open(path)
    return '\n'.join(p.get_text() for p in doc)

