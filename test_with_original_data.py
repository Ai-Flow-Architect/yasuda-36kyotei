def run():
    print("=" * 70)
    print("原本データを使った生成PDF vs 原本PDF 類似度検証")
    print("=" * 70)
    Path(OUTPUT_DIR).mkdir(exist_ok=True)
    summary = []

    for rec in RECORDS:
        orig_name = rec.pop("_orig")
        pat = rec["様式パターン"]
        company = rec["事業所名"]
        orig_path = f"{ORIGINALS_DIR}/{orig_name}"

        print(f"\n▶ 様式{pat} / {company}")
        print(f"  原本: {orig_name}")

        # PDF生成
        try:
            pdf_bytes = generate_pdf(rec)
            safe = re.sub(r'[\\/:*?"<>|（）\s]', '_', company)
            out_path = Path(OUTPUT_DIR) / f"36協定書_{safe}_{pat}.pdf"
            out_path.write_bytes(pdf_bytes)
            print(f"  生成: {out_path.name} ({len(pdf_bytes):,} bytes)")
        except Exception as e:
            print(f"  ❌ 生成エラー: {e}")
            import traceback; traceback.print_exc()
            summary.append({"様式": pat, "全体": "エラー", "条文平均": "エラー"})
            continue

        # 比較
        orig_text = extract_text(orig_path)
        gen_text  = extract_text(str(out_path))
        overall, art_avg, art_sims, orig_n, gen_n = compare(orig_text, gen_text)

        print(f"  条文数: 原本{orig_n}条 / 生成{gen_n}条")
        print(f"  テンプレ全体類似度: {overall*100:.1f}%")
        print(f"  条文平均類似度:      {art_avg*100:.1f}%")
        print("  条文別:")
        for k in sorted(art_sims.keys(), key=lambda x: int(re.search(r'\d+',x).group()) if re.search(r'\d+',x) else 0):
            s = art_sims[k]
            bar = "█" * int(s*20) + "░" * (20-int(s*20))
            flag = "✅" if s >= 0.80 else ("⚠️ " if s >= 0.60 else "❌")
            print(f"    {flag} {k:12s}: {s*100:5.1f}%  {bar}")

        summary.append({"様式": f"様式{pat}", "全体": f"{overall*100:.1f}%", "条文平均": f"{art_avg*100:.1f}%"})

    print("\n" + "=" * 70)
    print("サマリー（原本データで生成 vs 原本PDF）")
    print("=" * 70)
    print(f"\n{'様式':<10} {'テンプレ全体':>12} {'条文平均':>10}")
    print("─" * 36)
    for r in summary:
        print(f"  {r['様式']:<8} {r['全体']:>12} {r['条文平均']:>10}")

    valid = [r for r in summary if r["条文平均"] not in ("エラー", "N/A")]
    if valid:
        avg_art  = sum(float(r["条文平均"].rstrip("%")) for r in valid) / len(valid)
        avg_tmpl = sum(float(r["全体"].rstrip("%")) for r in valid) / len(valid)
        print(f"\n  平均 (テンプレ全体): {avg_tmpl:.1f}%")
        print(f"  平均 (条文単位):     {avg_art:.1f}%")

if __name__ == "__main__":
    run()
